function claro(){
    const fundo = document.getElementById("fundoSite")
    fundo.addEventListener('click', fundo.style.backgroundColor='#fdfffc',
    fundo.style.color='#011627')
}
function escuro(){
    const fundo = document.getElementById("fundoSite")
    fundo.addEventListener('click', fundo.style.backgroundColor='#111111', 
    fundo.style.color='#ffffff')
}